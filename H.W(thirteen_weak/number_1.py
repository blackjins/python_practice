'''
세 집합 A, B, C가 다음과 같이 주어졌다. 
A = {2, 4, 5}
B = {1, 2, 2, 1, 10, 4}
C = {3, 3, 3, 4}
A,B,C의 교집합을 구하여라
'''
A = {2, 4, 5}
B = {1, 2, 2, 1, 10, 4}
C = {3, 3, 3, 4}
# pass 와  None을 적절한 명령문과 표현식으로 대체하라.

AnBnC = A&B&C


assert AnBnC == {4}

#A,B,C의 합집합에서 A의 차집합을 더해라

# pass 와  None을 적절한 명령문과 표현식으로 대체하라.

ArBrCmA = (A | B | C) - A

assert ArBrCmA == {1, 3, 10}
